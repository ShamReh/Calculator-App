package calculator;

public class Calculator {

	public static int add(int a, int b) {
		int add = a + b;
		return add;

	}

	public static int subtract(int a, int b) {
		int subtract = a - b;
		return subtract;

	}

	public static int multiply(int a, int b) {
		int multiply = a * b;
		return multiply;
	}

	public static double divide(double a, double b) {
		double divide = a / b;
		return divide;
	}
}
